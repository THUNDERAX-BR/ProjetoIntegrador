    private void atualizar(){
        if(id!=-1){
            switch(tipo){
                case 'c' -> {
                    CategoriasDAO dao = new CategoriasDAO(Conector.conectar());
                    Categorias c = dao.categoria(id);
                    TxNome.setText(c.getNome());
                }
                case 'm' -> {
                    MovimentosDAO dao = new MovimentosDAO(Conector.conectar());
                    Movimentos m = dao.movimento(id);
                    TxNome.setText(m.getNome());
                }
                case 'e' -> {
                    EditorasDAO dao = new EditorasDAO(Conector.conectar());
                    Editoras e = dao.editora(id);
                    TxNome.setText(e.getNome());
                }
            }
        }
    }

