    private void BtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtSalvarActionPerformed
        if(!TxNome.getText().isEmpty()){
            switch(tipo){
                case 'c' -> {
                    CategoriasDAO dao = new CategoriasDAO(Conector.conectar());
                    if(id==-1){
                        dao.cadastrarCategoria(TxNome.getText());
                    }
                    else{
                        dao.alterarCategoria(id, TxNome.getText());
                    }
                }
                case 'm' -> {
                    MovimentosDAO dao = new MovimentosDAO(Conector.conectar());
                    if(id==-1){
                        dao.cadastrarMovimento(TxNome.getText());
                    }
                    else{
                        dao.alterarMovimento(id, TxNome.getText());
                    }
                }
                case 'e' -> {
                    EditorasDAO dao = new EditorasDAO(Conector.conectar());
                    if(id==-1){
                        dao.cadastrarEditora(TxNome.getText());
                    }
                    else{
                        dao.alterarEditora(id, TxNome.getText());
                    }
                }
            }
            listener.sinalglobal();
        }
        else{
            JOptionPane.showMessageDialog(this, "Preencha o campo.");
        }
    }//GEN-LAST:event_BtSalvarActionPerformed

