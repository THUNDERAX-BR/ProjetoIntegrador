    private void exibir(int id){
        LivrosDAO ldao = new LivrosDAO(Conector.conectar());
        Livros l = ldao.exibirLivro(id);
        this.livro = l;
        if(l.getCapa()!=null){
            LbCapa.setIcon(l.getCapa());
        }
        TxSinopse.setText(l.getDescricao());
        LbId.setText("ID: "+String.valueOf(l.getId()));
        LbEditora.setText(l.getNomeEditora());
        LbAutor.setText(l.getNomeAutor());
        LbAno.setText(String.valueOf(l.getAno()));
        LbTitulo.setText(l.getTitulo());
        
        CategoriasDAO cdao = new CategoriasDAO(Conector.conectar());
        ArrayList<Categorias> listac = cdao.categoriasLivro(id);
        for(Categorias c : listac){
            PnCategorias cat = new PnCategorias(c.getId(), c.getNome());
            PnCategorias.add(cat);
        }
        
        ExemplaresDAO edao = new ExemplaresDAO(Conector.conectar());
        ArrayList<PnExemplarInfoLivro> lista = edao.exemplaresLivro(id);
        
        if(lista != null){
            PnAux.removeAll();
            PnAux.setLayout(new BoxLayout(PnAux, BoxLayout.Y_AXIS));

            PnAux.add(Box.createVerticalStrut(30));
            for (int i = 0; i < lista.size(); i++) {
                JPanel p = lista.get(i);
                p.setAlignmentX(Component.CENTER_ALIGNMENT);
                PnAux.add(p);
                if (i < lista.size() - 1) {
                    PnAux.add(Box.createVerticalStrut(40));
                }
            }

            PnAux.revalidate();
            PnAux.repaint();
        }
    }
    
